import { CalendarDatum, ResponsiveCalendar } from '@nivo/calendar'
import { useCompletedBooksStatsQuery } from '@stump/client'
import dayjs from 'dayjs'
import groupBy from 'lodash.groupby'
import { useMemo } from 'react'

import { useChartColors } from '../useChartColors'

export default function CompletedBooksCalendar() {
	const { data } = useCompletedBooksStatsQuery()
	const {
		calendar: { legendColor, tooltipColor, ...calProps },
		colors,
	} = useChartColors()

	const groupedData = useMemo<CalendarDatum[]>(() => {
		const groupedByDate = groupBy(
			data.map((d) => ({
				...d,
				day: dayjs(d.completed_at).format('YYYY-MM-DD'),
			})),
			'day',
		)

		return Object.entries(groupedByDate).map(([day, values]) => ({
			day,
			value: values.length,
		}))
	}, [data])

	return (
		<div className="h-40">
			<ResponsiveCalendar
				data={groupedData}
				// data={MOCK_DATA}
				from={dayjs().startOf('year').toDate()}
				to={dayjs().toDate()}
				theme={{
					axis: {
						ticks: {
							text: {
								fill: legendColor,
							},
						},
					},
					labels: {
						text: {
							fill: legendColor,
						},
					},
					legends: {
						text: {
							fill: legendColor,
						},
					},
					tooltip: {
						container: {
							backgroundColor: tooltipColor,
							color: legendColor,
						},
					},
				}}
				colors={colors}
				yearSpacing={40}
				dayBorderWidth={2}
				legends={[
					{
						anchor: 'bottom-right',
						direction: 'row',
						itemCount: 4,
						itemDirection: 'right-to-left',
						itemHeight: 36,
						itemWidth: 42,
						itemsSpacing: 14,
						translateY: 36,
					},
				]}
				{...calProps}
			/>
		</div>
	)
}

